package com.Library.services.common;

/**
 * Enum representing the roles of users in the library system.
 */
public enum UserRole {
    /**
     * Super admin role with the highest privileges.
     */
    SUPER_ADMIN,

    /**
     * Admin role with elevated privileges.
     */
    ADMIN,

    /**
     * Regular user role with standard privileges.
     */
    USER
}
